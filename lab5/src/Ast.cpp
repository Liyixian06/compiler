void FunctionDef::genCode()
{
    Unit *unit = builder->getUnit();
    Function *func = new Function(unit, se);
    BasicBlock *entry = func->getEntry();
    // set the insert point to the entry basicblock of this function.
    builder->setInsertBB(entry);

    if(params) params->genCode();
    stmt->genCode();

    // 构造流图
    for(auto bb = func->begin(); bb!=func->end(); bb ++){
        Instruction* it = (*bb)->begin();
        Instruction* last = (*bb)->rbegin();
        // 删除条件型语句
        while(it!=last){
            if(it->isCond() || it->isUncond())
                (*bb)->remove(it);
            it = it->getNext();
        }
        if(last->isCond()){
            BasicBlock *true_bb = dynamic_cast<CondBrInstruction*>(last)->getTrueBranch();
            BasicBlock *false_bb = dynamic_cast<CondBrInstruction*>(last)->getFalseBranch();
            (*bb)->addSucc(true_bb);
            (*bb)->addSucc(false_bb);
            true_bb->addPred((*bb));
            false_bb->addPred((*bb));
            if(true_bb->empty()) new RetInstruction(nullptr, true_bb);
            if(false_bb->empty()) new RetInstruction(nullptr, false_bb);
        }
        else if (last->isUncond()){
            BasicBlock *br = dynamic_cast<UncondBrInstruction*>(last)->getBranch();
            (*bb)->addSucc(br);
            br->addPred((*bb));
            if(br->empty()){ // 如果无条件跳转的目标为空，就插入return
                if(((FunctionType*)(se->getType()))->getRetType() == TypeSystem::intType)
                    new RetInstruction(new Operand(new ConstantSymbolEntry(TypeSystem::intType, 0)), br);
                else if(((FunctionType*)(se->getType()))->getRetType() == TypeSystem::voidType)
                    new RetInstruction(nullptr, br);
            }
        }
        else if (!last->isRet()){
            // 最后一条指令不是返回，也没有后继，说明当前函数返回void，或者是main函数
            if(((FunctionType*)(se->getType()))->getRetType() == TypeSystem::voidType)
                new RetInstruction(nullptr, *bb);
        }
    }
   
}

