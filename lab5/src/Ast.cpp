void VarDef::genCode()
{
    IdentifierSymbolEntry *se = dynamic_cast<IdentifierSymbolEntry*>(id->getSymPtr());
    if(se->isGlobal()){
        Instruction* alloca;
        Operand* addr;
        SymbolEntry *addr_se;
        addr_se = new IdentifierSymbolEntry(*se); // 新建一个构造函数
        addr_se->setType(new PointerType(se->getType())); // 设为指针
        addr = new Operand(addr_se);
        se->setAddr(addr); // 存放变量地址信息
        if(expr)
            alloca = new GlobalAllocaInstruction(addr, se, expr->getOperand());
        else alloca = new GlobalAllocaInstruction(addr, se);
        Unit* unit = builder->getUnit();
        unit->push_global(alloca);
    }
    else if (se->isLocal()){
        Function *func = builder->getInsertBB()->getParent();
        BasicBlock *entry = func->getEntry();
        Instruction *alloca;
        Operand *addr;
        SymbolEntry *addr_se;
        Type *type;
        type = new PointerType(se->getType());
        addr_se = new TemporarySymbolEntry(type, SymbolTable::getLabel());
        addr = new Operand(addr_se);
        alloca = new AllocaInstruction(addr, se);                   // allocate space for local id in function stack.
        entry->insertFront(alloca);                                 // allocate instructions should be inserted into the begin of the entry block.
        se->setAddr(addr);                                          // set the addr operand in symbol entry so that we can use it in subsequent code generation.
        if(expr){
            expr->genCode();
            BasicBlock *bb;
            bb = builder->getInsertBB();
            new StoreInstruction(addr, expr->getOperand(), bb);
        }                                          
    }
}

