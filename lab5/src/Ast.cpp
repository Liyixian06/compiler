void BinaryExpr::genCode()
{
    BasicBlock *bb = builder->getInsertBB(); // 当前插入位置
    Function *func = bb->getParent(); // 当前块所在的函数
    if (op == AND)
    {
        expr1->getOperand()->getSymbolEntry()->setType(TypeSystem::boolType);
        expr2->getOperand()->getSymbolEntry()->setType(TypeSystem::boolType);
        BasicBlock *trueBB = new BasicBlock(func);  // if the result of lhs is true, jump to the trueBB.
        expr1->genCode();
        backPatch(expr1->trueList(), trueBB); // 将expr1为真的跳转地址设为trueBB
        builder->setInsertBB(trueBB);               // set the insert point to the trueBB so that intructions generated by expr2 will be inserted into it.
        expr2->genCode();
        // expr1为假时，expr2不会执行
        true_list = expr2->trueList();
        false_list = merge(expr1->falseList(), expr2->falseList()); // 二者之一为假则为假
    }
    else if(op == OR)
    {
        expr1->getOperand()->getSymbolEntry()->setType(TypeSystem::boolType);
        expr2->getOperand()->getSymbolEntry()->setType(TypeSystem::boolType);
        BasicBlock *trueBB = new BasicBlock(func);
        expr1->genCode();
        // 如果expr1为假，就跳转到expr2
        unbackPatch(expr1->falseList(), trueBB);
        builder->setInsertBB(trueBB);
        expr2->genCode();
        true_list = merge(expr1->trueList(), expr2->trueList()); // 二者之一为真则为真
        false_list = expr2->falseList();
    }
    // 关系比较
    else if(op >= EQUAL && op <= GREATEREQUAL)
    {
        expr1->genCode();
        expr2->genCode();
        Operand *src1 = expr1->getOperand();
        Operand *src2 = expr2->getOperand();
        int opcode = -1;
        switch(op)
        {
            case EQUAL:
                opcode = CmpInstruction::E;
                break;
            case NOTEQUAL:
                opcode = CmpInstruction::NE;
                break;
            case LESS:
                opcode = CmpInstruction::L;
                break;
            case GREATER:
                opcode = CmpInstruction::G;
                break;
            case LESSEQUAL:
                opcode = CmpInstruction::LE;
                break;
            case GREATEREQUAL:
                opcode = CmpInstruction::GE;
                break;
        }
        new CmpInstruction(opcode, dst, src1, src2, bb);
        // 实际上不执行什么
        BasicBlock *true_bb, *false_bb, *temp_bb;
        true_bb = new BasicBlock(func);
        false_bb = new BasicBlock(func);
        temp_bb = new BasicBlock(func);
        true_list.push_back(new CondBrInstruction(true_bb, temp_bb, dst, bb));
        false_list.push_back(new UncondBrInstruction(false_bb, temp_bb));
    }
    else if(op >= ADD && op <= MOD)
    {
        expr1->genCode();
        expr2->genCode();
        Operand *src1 = expr1->getOperand();
        Operand *src2 = expr2->getOperand();
        int opcode = -1;
        switch (op)
        {
        case ADD:
            opcode = BinaryInstruction::ADD;
            break;
        case SUB:
            opcode = BinaryInstruction::SUB;
            break;
        case MUL:
            opcode = BinaryInstruction::MUL;
            break;
        case DIV:
            opcode = BinaryInstruction::DIV;
            break;
        case MOD:
            opcode = BinaryInstruction::MOD;
            break;
        }
        new BinaryInstruction(opcode, dst, src1, src2, bb);
    }
}

void UnaryExpr::genCode()
{
    expr->genCode();
    BasicBlock* bb = builder->getInsertBB();
    Operand *src = expr->getOperand();
    int opcode = -1;
    switch(op)
    {
        case ADD:
            opcode = UnaryInstruction::ADD;
            break;
        case SUB:
            opcode = UnaryInstruction::SUB;
            break;
        case NOT:
            opcode = UnaryInstruction::NOT;
            break;
    }
    dst = new Operand(symbolEntry);
    if(opcode == UnaryInstruction::NOT){
        new XorInstruction(dst, src, bb);
        return;
    }
    new UnaryInstruction(opcode, dst, src, bb);
}

